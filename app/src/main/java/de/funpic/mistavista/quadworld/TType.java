package de.funpic.mistavista.quadworld;

public class TType {
	
	public Square square, objSquare;
	public float objXAngle, objZAngle;
	public int[][] typesToDrop; // these types will be "dropped" to the item bar, 
								// with count
	public int nextType;
	public int id;
	public int overClass;
	
	public TAnimation animation = null;
	
	public TType(Square square, Square objSquare, float objXAngle, float objZAngle, 
			int[][] typesToDrop, int nextType, int id) {
		this.square = square;
		this.objSquare = objSquare;
		this.objXAngle = objXAngle;
		this.objZAngle = objZAngle;
		this.id = id;
		this.typesToDrop = typesToDrop;
		this.nextType = nextType;
		overClass = -1;
	}

}
